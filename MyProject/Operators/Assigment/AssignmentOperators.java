package com.sachin.Operators.Assigment;

public class AssignmentOperators {
    public static void main(String[] args) {
        //Example 1: Assignment value to int  variable
        int intValue = 10;
        System.out.println("intvalue: " + intValue); // Output: doubleValue: 10

        //Example 2: Assignment value to double variable
        double doubleValue = 3.12;
        System.out.println("doubleValue: " + doubleValue); // Output: doubleValue: 3.12

        // Example 3: Assignment value to booleanValue variable
        boolean booleanValue = true;
        System.out.println("booleanValue: " + booleanValue); // Output: booleanValue: true

        // Example 4: Assignment value to charValue  variable
        char charValue = 'A';
        System.out.println("charValue: " + charValue); // Output: charValue: A

        // Example 5: Assignment value to StringValue variable
        String stringValue = "hello world";
        System.out.println("stringValue:" + stringValue ); // Output: stringValue: hello world
    }
}
