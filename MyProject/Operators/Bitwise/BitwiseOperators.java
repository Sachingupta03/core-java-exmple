package com.sachin.Operators.Bitwise;

public class BitwiseOperators {

    public static void main(String[] args) {
        // Bitwise AND (&)
        int num1 =5;
        int nub2 =10;

        // Example 1: Bitwise And
        int resultAnd1 = num1 & nub2;
        System.out.println("Bitwise AND Example 1:" + resultAnd1);

        // Example 2: Bitwise And
        int num3 = 15;
        int resultAnd2 = nub2 & num3;
        System.out.println("Bitwise AND Example 2:" + resultAnd2);

        // Bitwise OR(|)
        // Example 1: Bitwise OR
        int resultOr1 = num1 | nub2;
        System.out.println("Bitwise OR Example 1: +" + resultOr1);

        // Example 2: Bitwise OR
        int num4 = 20;
        int reusltOr2 = num3 | num4;
        System.out.println("Bitwise OR Example 2:" + reusltOr2);
    }
}

